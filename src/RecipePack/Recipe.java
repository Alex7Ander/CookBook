package RecipePack;

public class Recipe {
	private String name;
	private String type;
	private String text;
	private int countOfIngredients;
	
	public String getName() {
		return this.name;
	}
	
	public String getType() {
		return this.type;
	}
	
	public String getText() {
		return this.text;
	}
	
	public int getCountOfIngredients() {
		return this.countOfIngredients;
	}

}
